class Endereco {

    constructor(id, endereco, bairro, nro, estado, cidade, cep, tipoEndereco) {
        this.id = id;
        this.endereco = endereco;
        this.bairro = bairro;
        this.nro = nro;
        this.estado = estado;
        this.cidade = cidade;
        this.cep = cep;
        this.tipoEndereco = tipoEndereco;
    }
}